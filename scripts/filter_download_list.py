def main():
    to_download_list = set()
    mgr = YoutubeCache()
    meta_crawler = MetaCrawler()
    formatter = get_url_formatter("youtube")
    for url in request_list:
        extra = get_extraction(url)
        if extra.is_playlist:
            playlist = meta_crawler.retrieve_playlist(extra.url)
            for item in playlist:
                to_download_list.add(item)
        else:
            to_download_list.add(extra.parse_unique_id())
    
    filtered_list = [url for url in to_download_list]
    print(filtered_list)
    for cache in mgr.list_cache():
        print("%s complete:%u, left_over:%u" % (
            cache.unique_id, cache.is_complete, cache.left_over))
        if cache.unique_id in to_download_list and cache.is_complete:
            filtered_list.remove(cache.unique_id)
    
    for uid in filtered_list:
        print('"%s",' % formatter.get_playable_url(uid))


if __name__ == '__main__':
    main()
